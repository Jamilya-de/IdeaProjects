public class booleanisEven {

    public static void main(String[] args) {
        System.out.println(isEven(15));
        System.out.println(isEven(10));

    }

    public static boolean isEven(int number) {
        if (number % 2 == 0) {
            return true;
        } else {
            return false;
        }
    }

}










